// 辅助类型：递归地将深度嵌套的字面量类型转换为结构类型
type DeepStringify<T> = T extends string
  ? string
  : {
      [K in keyof T]: T[K] extends Record<string, any> ? DeepStringify<T[K]> : string
    }

